    {
        return (new MailMessage)
                    ->from('travelpulsecaravan@gmail.com', 'Accentic')
                    ->greeting('Bonjour!')
                    ->line('Accentic technology')
                    ->line('Un utilisateur vient de ce souscrire aux newsletter')
                    ->line('Accentic Admin!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @return array<string, mixed>
     */
    public function toArray(object $notifiable): array
    {
        return [
            //
        ];
    }
}
